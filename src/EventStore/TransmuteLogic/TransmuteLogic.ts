
const { forIn, extend } = require('lodash')

const jsonLogic = require('json-logic-js')

export module TransmuteLogic {

    export const apply = (rule, data) =>{
        return jsonLogic.apply(rule, data)
    }

    export const projection = (jsonLogicRule, objectStates) =>{
        return objectStates.map((objState) =>{
            return jsonLogic.apply(jsonLogicRule, objState)
        })
    }

}